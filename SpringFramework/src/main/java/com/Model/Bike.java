package com.Model;

public class Bike {

	private int bike_id;
	private String bike_name;
	private int bike_modelno;
	private int bike_price;

	public int getBike_id() {
		return bike_id;
	}
	public void setBike_id(int bike_id) {
		this.bike_id = bike_id;
	}
	public String getBike_name() {
		return bike_name;
	}
	public void setBike_name(String bike_name) {
		this.bike_name = bike_name;
	}
	public int getBike_modelno() {
		return bike_modelno;
	}
	public void setBike_modelno(int bike_modelno) {
		this.bike_modelno = bike_modelno;
	}
	public int getBike_price() {
		return bike_price;
	}
	public void setBike_price(int bike_price) {
		this.bike_price = bike_price;
	}
	
	
}
